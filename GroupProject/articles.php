							<h3>Holiday Celebrations Need to Respect Differences</h3>
							<p>
								The Christmas decorations are being hung in my department, soon to be followed by seasonal music, holiday treats, Secret Santas, and other Christmas traditions.   
								As the manager, I have supported holiday celebrations as a way to bring fun, festivity, and caring into our workplace but this year I'm already hearing some complaints.  
								Some are grumbling that all the merry-making just gets in the way of productivity while others are concerned about financial and other expectations.  
								Those who appreciate the traditions are calling them a bunch of Scrooges.  If celebrating the holidays is just going to cause divisions, 
								maybe I should just pull the plug on holiday activities.
							</p>
							<p>
								All work and no play makes for a dull, unfriendly workplace.  A little fun and good cheer go a long way toward making work enjoyable.  
								Problems often arise, however, when only some decide what fun, festivity and caring should look like, yet everyone is expected to participate.  
								Make this holiday season enjoyable for everyone with these tips.
							</p>
							<p>
								<span class="bold">Adjust attitudes. </span>  
								In any workplace there are likely to be different ideas about which holidays should be celebrated and how.  
								Help your staff to realize that conflicts resulting from differences don't get resolved with name calling and judgments about right and wrong.  
								When they can begin to respect each other's preferences, you can all make better decisions about what makes sense for your department. 
							</p>
							<p>
								<span class="bold">Be realistic about expectations. </span>  
								Some people may not celebrate Christmas due to religious, cultural or personal beliefs while others may be passionate about their own traditions.  
								Some may get energized by all of the activities; others may find it somewhat overwhelming in addition to their other responsibilities and commitments. 
								In any case, when one is expected to participate in something not of one's choosing, you can expect to encounter grumbling and even resentment.  
								Giving people a choice, and a way to opt out of activities without judgment, allows them to take care of themselves and to feel respected.
							</p>
							<p>
								<span class="bold">Set boundaries around celebrating. </span>
								A "festive" atmosphere creates distractions, both physical and emotional, that can get in the way of getting the job done.  
								While some people's work load may lighten during the holiday season, for others it can be a peak time because of seasonal demands, year-end requirements, or simply being new.  
								Make sure that the festivities are confined to places and times that are appropriate for all.
							</p>
							<p>
								A truly caring work environment honors and respects the needs of all.

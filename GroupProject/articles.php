							<h3>When You Understand Resistance to Change You Can Overcome It</h3>
							<p>
								In order to deal with the downturn in the economy we're going to have to 
								make some big changes within the next year.  If everybody gets on board, 
								we can continue to be profitable and no jobs will be lost.  Unfortunately, 
								many people here really hate change and I'm expecting a lot of resistance.  
								How can I set the stage to overcome the resistance?
							</p>
							<p>
								Change is inevitable and so is some resistance to change.  When you understand 
								the primary reasons why people resist change you will discover creative ways to 
								help your employees accept, and maybe even welcome, change.
							</p>
							<p>
								<span class="bold">Habit. </span>  
								We form habits to create consistency and predictability in our environment. 
								Change creates disorder as it requires forming new habits to replace old familiar ones.  
								Give people reasonable time to adjust and develop the necessary new habits.  
								It takes at least 21 consecutive days for a behavior to become a habit so don't 
								assume that lack of instant compliance is resistance.
							</p>
							<p>
								<span class="bold">Security. </span>  
								Workplace change is often perceived to threaten job security.  
								Listen carefully to employees concerns, fears, and insecurities so you can address them effectively.  
								Set clear policies and procedures to guide the change and provide stability.  
								Provide appropriate training and follow up to demonstrate your commitment to preserving current jobs.
							</p>
							<p>
								<span class="bold">Fear of the Unknown. </span>
								Letting go of the familiar to make room for the unknown is uncomfortable.  
								Employees may experience some of the same apprehension they felt when they first came on board.  
								Orient all employees to new ways of doing things utilizing similar practices as those used 
								to onboard new employees.  Include employees in decision-making whenever possible- it's hard to resist change you helped create.
							</p>
							<p>
								<span class="bold">Selective Information Processing: </span>
								People create a world that is comfortable for them and try to tune out information that alters it.  
								It's the old, "what I don't know can't hurt me" approach.  Develop a plan to communicate as much information as possible, as often as possible.  
								Repeat the message in a variety of formats - email, newsletter, stand-up talks, listening sessions, etc.  
								Because workers respond more positively to information shared directly by their immediate supervisor, 
								the attitude of that supervisor is critical.  An "I don't agree with this change but we're stuck with it,"  attitude sets the stage for resistance.  
								A positive attitude supportive of the change sets the stage for buy-in and employee support.
							</p>
							<p>
								When change is effectively managed, it is more likely for employees to embrace it than resist it.
							</p>
